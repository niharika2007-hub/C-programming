//selection statement to find the spelling of a number(if-else-if)
#include<stdio.h>
void main()
{
    int i;
    printf("enter a number");
    scanf("%d",&i);
    if(i==1)
    printf("One");
    else if(i==2)
    printf("Two");
    else if(i==3)
    printf("Three");
    else if(i==4)
    printf("Four");
    else if(i==5)
    printf("Five");
    else
    printf("Numbers out of limit");
}  

     

 
    