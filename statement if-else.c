//conditional statement for positive or negative (if-else)
#include<stdio.h>
void main()
{
    int a;
    printf("enter a value");
    scanf("%d",a);
    if(a>0)
    printf("a is positive number");
    else
    printf("a is negative number");
 } 
 
 
 
