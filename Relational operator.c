 //Relational operator
 #include<stdio.h>
 void main()
 {
     int a=10,b=5;
     printf("(a==b):%d\n",a==b);
     printf("(a!=b):%d\n",a!=b);
     printf("(a>b):%d\n",a>b);
     printf("(a<b):%d\n",a<b);
     printf("(a>=b):%d\n",a>=b);
     printf("(a<=b):%d\n",a<=b);
 }    